<h3>Réglement de la Team</h2>
<p>
    Afin de nous mettre tous sur la même page, il est nécessaire d'appliquer quelques règles.<br/>
    En rejoignant la team ou le Discord vous acceptez les règles de la team.
</p>

<ul>
    <li>Ne partagez pas d'informations personnelles;</li>
    <li>Poster du contenu (liens, images, symbol art) à caractère pornographique, gore ou illégal;</li>
    <li>Les propos racistes, incitant à la haine ou politiquement incorrect ne sont pas autorisé;</li>
    <li>Le harcelement et les insultes avec une personne ou un groupe de personnes sont interdites;</li>
    <li>L'utilisation d'outils interdits est strictement interdite (plus d'information plus bas).</li>
</ul>

<p>
    Utilisez votre bon sens pour juger de ce qui peut être interdit, vous pouvez contacter de façon un manageur si vous avez des doutes.<br/>
    Nous nous attendons à un comportement correct afin de ne pas faire tâcher l’image de la communauté francophone sur PSO2 JP.
</p>