<h2>Outils interdit</h2>
<p>
    Les outils strictement interdits par SEGA sont également strictement interdit dans la team.
</p>
<ul>
    <li>Cheats, hacks et exploitation de bugs;</li>
    <li>Lecteurs et manipulateurs de chances;</li>
    <li>Damage Parser (parseur) ou DPS Meter.</li>
</ul>
<p>
    Le parseur fait partie des outils d'analyses que <a href="http://pso2.jp/players/news/?id=9224">SEGA a formellement interdit</a> (<a href="http://bumped.org/psublog/regarding-use-of-third-party-analytic-tools/">source anglaise</a>). Cet outil est donc interdit afin de ne pas nuire à l'ambiance de la team en discriminant ou critiquant les chiffres d'un autres joueur de la team ou extérieur à la team.<br/>
    D'autres outils peuvent être ajouté à cette liste à tout moment, cette liste ne représente pas une liste exhaustive des outils interdits.
</p>

<p>
    Pour une liste non exhaustive des outils et modifications qui ne sont pas interdites:
</p>
<ul>
    <li>Les lanceurs alternatif;</li>
    <li>Simulateurs de dégats, de skill tree, d'affix (liens disponible sur Discord);</li>
    <li>Les plugins de traductions;</li>
    <li>Les mods de textures et de models;</li>
    <li>Les shaders alternatif (ReShade, SweetFX...).</li>
</ul>

<p>
    Gardez en tête que même si SEGA n'interdit pas explicitement certains outils il reste possible que ces outils altèrent le fonctionnement du jeu et le rende instable voir injouable.
</p>