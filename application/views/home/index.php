    <h2>
        Réglement de la Team
    </h2>
    <p>
        En rejoignant la team vous acceptez les règles de la team.
    </p>
    <h3>
        Contenu interdit
    </h3>
    <p>
        Il est formellement interdit de:
    </p>
    <ul>
        <li>Partager des informations personnelles;</li>
        <li>Poster du contenu (liens, symbol art...) pornographique et/ou hentai;</li>
        <li>Poster du propos racistes et/ou portant atteinte à une personne ou un groupe de personnes;</li>
        <li>Harceler une personne ou un groupe de personnes faisant partie ou pas de la team;</li>
        <li>Faire preuve de la moindre toxicité envers la team ou les joueurs externe.</li>
    </ul>
    <p>
        Utilisez votre bon sens pour juger de ce qui peut être interdit, vous pouvez contacte de façon privé un manageur si vous avez des doutes. Ces règles sont valable en <span class="highlight">All Chat</span>, <span class="highlight">Party Chat</span> et <span class="highlight">Team Chat</span>.<br />
        Toutes demandes de support doit se faire publiquement sur le chan <a href="https://discordapp.com/channels/318187257171279873/318189110789537793">#support</a> de notre <a routerLink="/discord">Discord</a> car tout le monde peut aider, le support par MP est interdit et vous sera refusé.
    </p>
    <p>
        Nous nous attendons à un comportement correct afin de ne pas faire tâcher l’image de la communauté étrangère sur PSO2.
    </p>
    <h3>
        Outils interdit
    </h3>
    <p>
        Les outils strictement interdits par SEGA sont également strictement interdit dans la team.
    </p>
    <ul>
    <li>Cheats et hacks;</li>
    <li>Damage Parser (parseur) ou DPS Meter.</li>
    </ul>
    <p>
        Si vous utilisez un outil interdit vous vous exposez à être retirer de la team de façon immédiate.
    </p>
    <p>
        Le parseur fait partie des outils d'analyses que <a href="http://pso2.jp/players/news/?id=9224">SEGA a formellement interdit</a> (<a href="http://bumped.org/psublog/regarding-use-of-third-party-analytic-tools/">source anglaise</a>). Cet outil est donc interdit afin de ne pas nuire à l'ambiance de la team en discriminant ou critiquant les chiffres d'un autres joueur de la team ou extérieur à la team.
    </p>

